# Decimal to Binary, Octal, Hexadecimal
a = 1234
print('Decimal to Binary : ', bin(a))
print('Decimal to Octal : ', oct(a))
print('Decimal to Hexadecimal : ', hex(a))

# Binary to Decimal, Octal, Hexadecimal
a = 0b11101
print('Binary to Decimal : ', a)
print('Binary to Octal : ', oct(a))
print('Binary to Hexadecimal : ', hex(a))

# Octal to Decimal, Binary, Hexadecimal
a = 0o1237
print('Octal to Decimal : ', a)
print('Octal to Binary : ', bin(a))
print('Octal to Hexadecimal : ', hex(a))

# Hexadecimal to Decimal, Binary, Octal
a = 0xABFACE12
print('Hexadecimal to Decimal : ', a)
print('Hexadecimal to Binary : ', bin(a))
print('Hexadecimal to Octal : ', oct(a))

